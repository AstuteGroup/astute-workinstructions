#!/usr/bin/env node
/**
 * Market Offer Email Sender
 *
 * Sends extracted market offer CSV files to jake.harris@astutegroup.com
 * Subject format: "[Partner Name]/[Search Key], Market Offer Upload Ready"
 *
 * Usage:
 *   node send-offer-email.js <csv_file> <partner_name> <search_key>
 *   node send-offer-email.js output/OFFER_UPLOAD_20260317_Celestica_CMY2.csv "Celestica" "1001118"
 */

const fs = require('fs');
const path = require('path');
const { spawn } = require('child_process');

// Configuration
const EMAIL_CONFIG = {
    account: 'excess',
    recipient: 'jake.harris@astutegroup.com',
    himalayaBin: process.env.HIMALAYA_BIN || path.join(process.env.HOME, 'bin', 'himalaya')
};

/**
 * Send email with attachment using himalaya MML template
 */
function sendEmail(to, subject, body, attachments = []) {
    // Build MML template
    let mml = `From: ${EMAIL_CONFIG.account}@orangetsunami.com
To: ${to}
Subject: ${subject}

<#part type=text/plain>
${body}
<#/part>`;

    // Add attachments
    for (const attachment of attachments) {
        if (fs.existsSync(attachment)) {
            mml += `
<#part filename="${attachment}" disposition=attachment>
<#/part>`;
        } else {
            console.error(`  Warning: Attachment not found: ${attachment}`);
        }
    }

    return new Promise((resolve) => {
        console.log(`  Sending email to ${to}`);
        console.log(`  Subject: ${subject}`);

        const proc = spawn(EMAIL_CONFIG.himalayaBin, [
            'template', 'send',
            '--account', EMAIL_CONFIG.account
        ], {
            env: process.env,
            stdio: ['pipe', 'pipe', 'pipe']
        });

        let stderr = '';
        proc.stderr.on('data', (data) => { stderr += data.toString(); });

        proc.on('close', (code) => {
            if (code === 0) {
                console.log(`  ✓ Email sent successfully`);
                resolve(true);
            } else {
                console.error(`  ✗ Failed to send email: ${stderr}`);
                resolve(false);
            }
        });

        proc.on('error', (err) => {
            console.error(`  ✗ Failed to send email: ${err.message}`);
            resolve(false);
        });

        // Write template to stdin and close
        proc.stdin.write(mml);
        proc.stdin.end();
    });
}

/**
 * Count lines in CSV file (excluding header)
 */
function countLines(csvPath) {
    const content = fs.readFileSync(csvPath, 'utf8');
    const lines = content.trim().split('\n');
    return lines.length - 1; // Exclude header
}

/**
 * Send market offer upload notification
 */
async function sendOfferNotification(csvPath, partnerName, searchKey) {
    const filename = path.basename(csvPath);
    const lineCount = countLines(csvPath);
    const dateStr = new Date().toISOString().split('T')[0];

    const subject = `${partnerName}/${searchKey}, Market Offer Upload Ready`;

    const body = `Market Offer Upload Ready for Import

Partner: ${partnerName}
Search Key: ${searchKey}
File: ${filename}
Lines: ${lineCount.toLocaleString()}
Date: ${dateStr}

Attached CSV is in Market Offer Line Import Template format, ready for iDempiere upload.

---
Generated by Market Offer Uploading workflow`;

    const success = await sendEmail(
        EMAIL_CONFIG.recipient,
        subject,
        body,
        [csvPath]
    );

    return { success, partnerName, searchKey, filename, lineCount };
}

/**
 * Send multiple offer files in batch
 * @param {Array} offers - Array of {csvPath, partnerName, searchKey}
 */
async function sendBatch(offers) {
    console.log('='.repeat(60));
    console.log('MARKET OFFER EMAIL SENDER');
    console.log('='.repeat(60));
    console.log(`Sending ${offers.length} offer file(s) to ${EMAIL_CONFIG.recipient}`);
    console.log('-'.repeat(60));

    const results = [];
    for (const offer of offers) {
        console.log(`\nProcessing: ${offer.partnerName}`);
        const result = await sendOfferNotification(
            offer.csvPath,
            offer.partnerName,
            offer.searchKey
        );
        results.push(result);
    }

    console.log('\n' + '='.repeat(60));
    console.log('SUMMARY');
    console.log('='.repeat(60));

    let successCount = 0;
    let totalLines = 0;
    for (const r of results) {
        const status = r.success ? '✓' : '✗';
        console.log(`${status} ${r.partnerName} (${r.searchKey}): ${r.lineCount} lines`);
        if (r.success) successCount++;
        totalLines += r.lineCount;
    }

    console.log('-'.repeat(60));
    console.log(`Sent: ${successCount}/${results.length} emails`);
    console.log(`Total lines: ${totalLines.toLocaleString()}`);

    return results;
}

// CLI interface
if (require.main === module) {
    const args = process.argv.slice(2);

    if (args.length < 3) {
        console.log('Usage: node send-offer-email.js <csv_file> <partner_name> <search_key>');
        console.log('       node send-offer-email.js --batch <json_file>');
        console.log('\nExamples:');
        console.log('  node send-offer-email.js output/OFFER_UPLOAD_20260317_Celestica_CMY2.csv "Celestica" "1001118"');
        console.log('\nBatch JSON format:');
        console.log('  [{"csvPath": "...", "partnerName": "...", "searchKey": "..."}, ...]');
        process.exit(1);
    }

    if (args[0] === '--batch') {
        // Batch mode: read JSON file with array of offers
        const batchFile = args[1];
        const offers = JSON.parse(fs.readFileSync(batchFile, 'utf8'));
        sendBatch(offers).then(results => {
            const allSuccess = results.every(r => r.success);
            process.exit(allSuccess ? 0 : 1);
        });
    } else {
        // Single file mode
        const [csvPath, partnerName, searchKey] = args;
        sendOfferNotification(csvPath, partnerName, searchKey).then(result => {
            process.exit(result.success ? 0 : 1);
        });
    }
}

module.exports = { sendOfferNotification, sendBatch, sendEmail };
